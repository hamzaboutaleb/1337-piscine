#include "ft.h"

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

void	ft_putstr(char *s, int end)
{
	while (*s)
		write(1, s++, 1);
	if (end == 1)
		write(1, "\n", 1);
}
