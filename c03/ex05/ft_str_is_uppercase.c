int	ft_str_is_uppercase(char *str)
{
	int		i;
	char	c;

	i = 0;
	while (str[i])
	{
		c = str[i];
		if (c < 'A' || c > 'Z')
		{
			return (0);
		}
		i++;
	}
	return (1);
}
